    private void btn_robotActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_robotActionPerformed
        isRunning = true;
        thread = new Thread(() -> {
            try {
                RobotJCR.AltTab(50);
                Thread.sleep(2000);
                for (int x = 0; x < jTable1.getRowCount(); x++) {
                    if (!isRunning) {
                        break;
                    }
                    try {
                        String codigo = getCodeSelected(x);
                        String un = "UN";
                        String valor = "1";
                        for (Product p : products) {
                            if (p.getCode().equals(codigo)) {
                                un = p.getUnity();
                                double v = p.getCost_value();
                                if (v == 0) {
                                    v = 1;
                                }
                                valor = Double.toString(CDbl.CDblDuasCasas(v));
                                break;
                            }
                        }
                        String quantidade = getAmountSelected(x, un);
                        if (!quantidade.equals("0")) {
                            RobotJCR.executar(codigo, (int) delay.getValue());
                            RobotJCR.executar(KeyEvent.VK_ENTER, (int) delay.getValue());

                            RobotJCR.executar(quantidade, (int) delay.getValue());
                            RobotJCR.executar(KeyEvent.VK_ENTER, (int) delay.getValue());

                            RobotJCR.executar(valor, (int) delay.getValue());
                            RobotJCR.executar(KeyEvent.VK_ENTER, (int) delay.getValue());

                        }
                    } catch (Exception e) {
                        System.err.println(e);
                    }
                }
            } catch (InterruptedException ex) {
                Logger.getLogger(ExchangeJF.class.getName()).log(Level.SEVERE, null, ex);
            }
            btn_removeExchange.setEnabled(true);
        });
        thread.start();
    }//GEN-LAST:event_btn_robotActionPerformed

