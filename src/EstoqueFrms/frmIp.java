    public final void preencherComboBox(){
        List<Usuario> usuarios = new UsuarioDAO().findAll();
        for (Usuario u:usuarios){
            if (u.getPermissao()==1){
                jComboBox1.addItem(u.getNome());
            }
        }
    }
    public void preencherTable(){
        List<IpUser> ips = new IpUserDAO().findAll();
        for (IpUser ip:ips){
            Object[] dado = {ip.getId(),ip.getIp(),ip.getUser()};
            tb.addRow(dado);
        }
    }
