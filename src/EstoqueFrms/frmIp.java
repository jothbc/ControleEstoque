    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            IpUser ip = new IpUser(iptxt.getText(),jComboBox1.getSelectedItem().toString());
            if (new IpUserDAO().addIp(ip)){
                tb.setRowCount(0);
                preencherTable();
                iptxt.setText("");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao tentar cadastar ip!","ERRO",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow()>=0){
            if (new IpUserDAO().delete((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0))){
                tb.removeRow(jTable1.getSelectedRow());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao tentar remover no servidor!","ERRO",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

