    private void passtxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passtxtKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER){
            String senha = CriptografiaMD5.criptografar(new String (passtxt.getPassword()));
            if (new UsuarioDAO().verificarPermissao(senha)){
                op=1;
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Senha inv√°lida!");
            }
        }
        if (evt.getKeyCode()==KeyEvent.VK_ESCAPE){
            this.dispose();
        }
    }//GEN-LAST:event_passtxtKeyPressed

