    public void preencherComboBox(){
        List<ProdutoFornecedor> produtos = new ProdutoFornecedorDAO().findAll();
        for (int x = 0; x < produtos.size(); x++) {
            boolean verificacao = true;
            for (int y = 0; y < jComboBox1.getItemCount(); y++) {
                if (produtos.get(x).getFornecedor().equals(jComboBox1.getItemAt(y))) {
                    verificacao = false;
                    break;
                }
            }
            if (verificacao) {
                jComboBox1.addItem(produtos.get(x).getFornecedor());
            }
        }
    }
