    public void preencherGrupoTb() {
        grupotb.setRowCount(0);
        for (Condicao g : grupo) {
            Object[] dado = {g.getTp(), g.getDecricao()};
            grupotb.addRow(dado);
        }
    }

    public void preencherProdutoTb(int grp) {
        produtotb.setRowCount(0);
        for (Produto p : produtos) {
            if (p.getGrupo() == grp) {
                Object[] dado = {p.getCodigo(), p.getDescricao(), p.getQtd(), p.getCusto(), p.getUnidade(), null};
                if (!"".equals(maiorque.getText())) {
                    long maior = Long.parseLong(maiorque.getText());
                    long valor = Long.parseLong(p.getCodigo());
                    if (valor >= maior) {
                        produtotb.addRow(dado);
                    }
                } else if (!"".equals(menorque.getText())) {
                    long menor = Long.parseLong(menorque.getText());
                    long valor = Long.parseLong(p.getCodigo());
                    if (valor <= menor) {
                        produtotb.addRow(dado);
                    }
                } else {
                    produtotb.addRow(dado);
                }
            }
        }
    }

