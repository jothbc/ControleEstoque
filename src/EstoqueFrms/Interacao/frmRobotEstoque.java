    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (delayAlt.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta um tempo para o delay do Alt+Tab.");
            return;
        }
        if (delayEscrita.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta um tempo para o delay da escrita.");
            return;
        }
        String valor = "1", qtd = null;
        if (!valorZero.getText().equals("")) {
            valor = valorZero.getText();
        }
        if (!qtdMais.getText().equals("")) {
            qtd = qtdMais.getText();
        }
        thRobot2 r = new thRobot2(produtotb, valor, qtd, Integer.parseInt(delayEscrita.getText()));
        th = new Thread(r);
        try {
            RobotJCR.AltTab(Integer.parseInt(delayAlt.getText()));
            if (!delayini.getText().equals("")) {
                Thread.sleep(Integer.parseInt(delayini.getText()));
            }
        } catch (InterruptedException ex) {
            Logger.getLogger(frmRobotEstoque.class.getName()).log(Level.SEVERE, null, ex);
        }
        th.start();
    }//GEN-LAST:event_jButton2ActionPerformed

