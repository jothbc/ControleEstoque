    private void mudarSeletor() {
        //local 5 = X
        if (jSlider1.getValue() == 1) {
            for (int x = 0; x < jTable2.getRowCount(); x++) {
                if (verificarUnd(x)) {
                    if ((double) jTable2.getValueAt(x, 2) < 0) {
                        jTable2.setValueAt("X", x, 5);
                    } else {
                        jTable2.setValueAt(null, x, 5);
                    }
                } else {
                    jTable2.setValueAt(null, x, 5);
                }
            }
        } else if (jSlider1.getValue() == 2) {
            for (int x = 0; x < jTable2.getRowCount(); x++) {
                if (verificarUnd(x)) {
                    if ((double) jTable2.getValueAt(x, 2) > 0) {
                        jTable2.setValueAt("X", x, 5);
                    } else {
                        jTable2.setValueAt(null, x, 5);
                    }
                } else {
                    jTable2.setValueAt(null, x, 5);
                }
            }
        }
        contarSelecionados();
    }

    private void contarSelecionados() {
        double totalSelect = 0;
        for (int x = 0; x < jTable2.getRowCount(); x++) {
            if (jTable2.getValueAt(x, 5) == "X") {
                totalSelect++;
            }
        }
        totalSelecionadotxt.setText("" + totalSelect);
    }

    private boolean verificarUnd(int x) {
        if (jTable2.getValueAt(x, 4) != "KG") {
            double qtd = (double) jTable2.getValueAt(x, 2);
            if ((int)qtd == 0) {
                return false;
            }
        }
        return true;
    }

