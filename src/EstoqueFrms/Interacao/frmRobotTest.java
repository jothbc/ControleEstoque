    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int op = -1;
        op = JOptionPane.showOptionDialog(this, "Deseja realmente excluir selecionados?\nOs itens também serão deletados do banco de dados.", "Excluir", JOptionPane.YES_NO_CANCEL_OPTION, 1, null, null, null);
        if (op == 0) {
            for (int x = 0; x < jTable1.getRowCount(); x++) {
                if ("X".equals((String)jTable1.getValueAt(x, 6))) {
                    if (!new ProdutoDAO().remove((int) jTable1.getValueAt(x, 4))) {
                        String temp = (String) jTable1.getValueAt(x, 4);
                        JOptionPane.showMessageDialog(this, "Erro ao tentar deletar o produto de id: " + temp + " no banco de dados, atualize a tabela!");
                        return;
                    }
                }
            }
        }
        baixas = new ProdutoDAO().findALL();
        baixas = new ProdutoDAO().obterCusto(baixas);
        unidades = new ProdutoDAO().getAllProtudosFirebird();
        for (int y = 0; y < unidades.size(); y++) {
            for (int x = 0; x < baixas.size(); x++) {
                if (baixas.get(x).getCodigo().equals(unidades.get(y).getCodigo())) {
                    baixas.get(x).setUnidade(unidades.get(y).getUnidade());
                }
            }
        }
        jButton1ActionPerformed(null);
        JOptionPane.showMessageDialog(null, "Concluido!");
    }//GEN-LAST:event_jButton4ActionPerformed

