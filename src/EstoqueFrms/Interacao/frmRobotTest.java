    public void setTxts() {
        double qtdtotal = 0, valortotal = 0;
        int totalitens = 0;
        for (int x = 0; x < jTable1.getRowCount(); x++) {
            if (jTable1.getValueAt(x, 6) == "X") {
                totalitens++;
                qtdtotal += ((double) jTable1.getValueAt(x, 1));
                valortotal += ((double) jTable1.getValueAt(x, 1) * (double) jTable1.getValueAt(x, 3));
            }
        }

        qtdtxt.setText(Integer.toString(totalitens));
        qtdtotaltxt.setText(Double.toString(CDbl.CDblTresCasas(qtdtotal)));
        valortotaltxt.setText(Double.toString(CDbl.CDblDuasCasas(valortotal)));
    }

    public void preencherTb(int tp) {
        tb.setRowCount(0);
        estoqueDisponivel = new EstoqueDAO().getEstoque();
        for (Produto p : baixas) {
            if (p.getTp() == tp) {
                double qtdDisp = 0;
                for (Estoque e : estoqueDisponivel) {
                    if (e.getCd().equals(p.getCodigo())) {
                        qtdDisp = e.getQtd_estoque();
                    }
                }
                String x = null;
                if (p.getQtd() <= qtdDisp) {
                    x = "X";
                }
                Object[] dado = {p.getCodigo(), p.getQtd(), qtdDisp, p.getCusto(), p.getId(), p.getUnidade(), x,p.getDescricao()};
                tb.addRow(dado);
            }
        }
        setTxts();
    }

