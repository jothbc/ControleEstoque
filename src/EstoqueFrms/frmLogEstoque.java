    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!cdtxt.equals("")){
            List<LogProduto> logProduto = new LogEstoque().getLogProduto(cdtxt.getText());
            CDate conv =  new CDate();
            for (LogProduto p:logProduto){
                Object[] dado = {conv.DataMySQLtoDataStringPT(p.getData()),p.getQtdAnterior(),p.getQtdAtual(),p.getQtdEntrada(),p.getMovimento()};
                tb.addRow(dado);
             }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

