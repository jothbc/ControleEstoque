    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        if (jList1.getSelectedIndex() >= 0) {
            int tp = 0;
            for (Condicao c : condicoes) {
                if (c.getDecricao().equals(jList1.getSelectedValue())) {
                    tp = c.getTp();
                }
            }
            atualizarTb(tp);
        }
    }//GEN-LAST:event_jList1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jList1.getSelectedIndex() >= 0) {
            int tp = 0;
            for (Condicao c : condicoes) {
                if (c.getDecricao().equals(jList1.getSelectedValue())) {
                    tp = c.getTp();
                }
            }
            if (tp != 0) {
                if (!new ProdutoDAO().removeProdutosCondicao(tp)) {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar remover itens do banco de dados!");
                }
            }
            atualizarTb(0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() >= 0) {
            jSpinnerqtd.setValue(jTable1.getValueAt(jTable1.getSelectedRow(), 2));
            if (!"X".equals(jTable1.getValueAt(jTable1.getSelectedRow(), 5))) {
                jTable1.setValueAt("X", jTable1.getSelectedRow(), 5);
            } else {
                jTable1.setValueAt(null, jTable1.getSelectedRow(), 5);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        for (int x = 0; x < jTable1.getRowCount(); x++) {
            if ("X".equals(jTable1.getValueAt(x, 5))) {
                if (!new ProdutoDAO().remove((int) jTable1.getValueAt(x, 0))) {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar remover o item de ID: " + jTable1.getValueAt(x, 0));
                }
            }
        }
        int tp = 0;
        for (Condicao c : condicoes) {
            if (c.getDecricao().equals(jList1.getSelectedValue())) {
                tp = c.getTp();
            }
        }
        atualizarTb(tp);

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
            return;
        }
        Produto p = new Produto();
        p.setId((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
        p.setQtd((double) jSpinnerqtd.getValue());
        if (new ProdutoDAO().updateQTD(p)) {
            int tp = 0;
            for (Condicao c : condicoes) {
                if (c.getDecricao().equals(jList1.getSelectedValue())) {
                    tp = c.getTp();
                }
            }
            atualizarTb(tp);
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao tentar atualizar quantidade no banco de dados!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

