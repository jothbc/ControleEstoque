    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Primeiro selecione uma linha.");
            return;
        }
        int seq, tp;
        if (evt.getKeyCode() == KeyEvent.VK_1 && CheckBoxAlterarTipo.isSelected()) {
            seq = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            tp = 1;
            if (new ProdutoDAO().updateTIPO(seq, tp)) {
                atualizarTb(0);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar mudar tipo no banco de dados!");
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_2 && CheckBoxAlterarTipo.isSelected()) {
            seq = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            tp = 2;
            if (new ProdutoDAO().updateTIPO(seq, tp)) {
                atualizarTb(0);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar mudar tipo no banco de dados!");
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_3 && CheckBoxAlterarTipo.isSelected()) {
            seq = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            tp = 3;
            if (new ProdutoDAO().updateTIPO(seq, tp)) {
                atualizarTb(0);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar mudar tipo no banco de dados!");
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_4 && CheckBoxAlterarTipo.isSelected()) {
            seq = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            tp = 4;
            if (new ProdutoDAO().updateTIPO(seq, tp)) {
                atualizarTb(0);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar mudar tipo no banco de dados!");
            }
        }
    }//GEN-LAST:event_jTable1KeyPressed

