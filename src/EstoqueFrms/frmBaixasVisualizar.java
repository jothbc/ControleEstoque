    private void atualizarTb(int tp) {
        tb.setRowCount(0);
        String cond;
        produtos = new ProdutoDAO().findALL();
        for (int x = 0; x < produtos.size(); x++) {
            cond = condFind(produtos.get(x).getTp(), condicoes);
            Object[] dado = {produtos.get(x).getId(), produtos.get(x).getCodigo(), produtos.get(x).getQtd(), produtos.get(x).getDescricao(), cond};
            if (produtos.get(x).getTp() == tp) {
                tb.addRow(dado);
            } else if (tp == 0) {
                tb.addRow(dado);
            }
        }
    }

    private String condFind(int tp, List<Condicao> cond) {
        for (int x = 0; x < cond.size(); x++) {
            if (cond.get(x).getTp() == tp) {
                return cond.get(x).getDecricao();
            }
        }
        return null;
    }

